package vn.edu.poly.thithu.model;

public class Model {
   String a;
   String b;
   String c;

    public Model() {
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public Model(String a, String b, String c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
}
